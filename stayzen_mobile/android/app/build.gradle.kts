            val sfName = keystoreProperties.getProperty("storeFile")
            if (sfName != null) {
                storeFile = file(sfName)
            }
            storePassword = keystoreProperties.getProperty("storePassword")
        }
    }

    defaultConfig {
        applicationId = "com.stayzen.sagiam.stayzen_flutter_app"
        minSdk = flutter.minSdkVersion
